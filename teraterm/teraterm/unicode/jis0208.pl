#!/usr/bin/perl
use strict;
use utf8;

my $fname_in = "JIS0208.txt";
my $fname_out = "jis0208.ini";
if (@ARGV == 2) {
	$fname_in = $ARGV[0];
	$fname_out = $ARGV[1];
} else {
	my $n = @ARGV;
	printf "argv $n\n";
	exit 1;
}

my @unicode_array = ();

open(FILE_IN, $fname_in) || die "Cannot open $fname_in.";
open(FILE_OUT, ">:crlf:utf8", $fname_out) || die "Cannot open $fname_out.";
print FILE_OUT "\x{feff}";	# BOM
print FILE_OUT "// this file was generated by jis0208.pl\n";

while(my $a = <FILE_IN>) {
	if ($a =~ /^0x([0-9A-Fa-f]+)\s+0x([0-9A-Fa-f]+)\s+0x([0-9A-Fa-f]+)\s+#\s*(.+)$/) {
		my $sjis = hex $1;
		my $jis = hex $2;
		my $unicode = hex $3;
		my $name = $4;
		if ($unicode >= 0x80) {
			push @unicode_array, $unicode;
		}
	}
}
close(FILE_IN);

my @sorted_unicode = sort { $a <=> $b } @unicode_array;

# 出力
my $index = 1;
my $start = 0;
my $end = 0;
foreach my $u (@sorted_unicode) {
	if ($start == 0) {
		$start = $u;
		$end = $u;
	} elsif ($u == $end + 1) {
		$end = $u;
	} else {
		if ($start == $end) {
			printf FILE_OUT ("Range%d=U+%06x, 2\n", $index, $start);
		} else {
			printf FILE_OUT ("Range%d=U+%06x, U+%06x, 2\n", $index, $start, $end);
		}
		$start = $u;
		$end = $u;
		$index += 1;
	}
}
